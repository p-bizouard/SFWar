<b><big>Pr&eacute;sentation</big></b>
</span>		

<br /><br />

<span>
<img style="float:right; padding-left:10px;width:200px;height:200px;" alt="Image avec un arbre des alliances/races" src="images/patch.gif" />

Bonjour &agrave; tous et bienvenue sur SF-War.
Vous allez entrer dans le monde fabuleux de Stargate SG-1, Atlantis et Battlestar Galactica, Combattant aupr&egrave;s des Tauris : terriens et Jaffas libre, aupr&egrave;s des Goa'uld souhaitant r&eacute;tablir l'ancien r&eacute;gime, des Oris, ou encore des cylons, des robots cr&eacute;&eacute;s par l'homme mais qui se sont r&eacute;volt&eacute;s il y a plusieurs centaines d'ann&eacute;es.
<br /><br />
<b>Pourquoi avoir mis ensemble Stargate et Battlestar Galactica : ce sont deux mondes compl&egrave;tements diff&eacute;rents, non ?</b><br />
En effet, et &agrave; la base, le jeu ne devait comporter que des clans issus de Stargate, mais en regardant les &eacute;pisodes de BSG, nous avons &eacute;t&eacute; conquis par leur monde, si lointain, mais &agrave; la fois si proche de Stargate, et nous avons fini par les inclure dans ce jeu.
<br /><br />
<b>Quel est le but de ce jeu ?</b><br />
En prenant partit dans l'un des trois clans (ou alliances) de cette galaxie, vous pouvez contr&ocirc;ler un empire d'une race de votre choix (Tauri, Goa'uld, Ori, Bsg ou cylons), construire une armada, et capturer des objectifs strat&eacute;giques afin de faire monter votre clan en puissance.
<br />
Attention, les alliances regroupent sous leur &eacute;tendard plusieurs races, ainsi des Tauris peuvent &ecirc;tre alli&eacute;s a des Oris.

</span>

<br /><br />

<span style="border-top:2px solid #808080;border-bottom:2px solid #808080;">
<b><big>Objectifs</big></b>
</span>

<br /><br />

<span>
<img style="float:left; padding-right:10px;width:200px;height:200px;" alt="Image de la plapla m&egrave;re" src="images/plapla_mere_index.png" />

Vous venez de vous inscrire, et vous faites maintenant partit d'un clan, et vous avez une race, parmi les 5 cit&eacute;s ci-dessus.<br />
Votre clan vient de se former, et n'a pas encore de nom, ni d'histoire !!<br />
Pour cela, vous devrez combattre aupr&egrave;s de vos camarades, afin de conqu&eacute;rir des territoires, en d&eacute;fendre d'autres, ou en accomplissant moult qu&ecirc;tes.<br />
L'objectif de chaque clans en ce d&eacute;but de partie sera de capturer une plan&egrave;te, au centre de la carte, et de la garder. Si votre clan poss&egrave;de la majorit&eacute; des cases de cette plan&egrave;te au bout d'un mois de jeu, alors votre clan sera proclam&eacute; vainqueur, et b&eacute;n&eacute;ficiera de bonus lors de la partie suivante.<br />
Les noms des clans seront d&eacute;cid&eacute;s PAR LES JOUEURS au bout de deux semaines de jeu, via le forum officiel (propositions et sondage).
</span>
